const allPlayers = [];
const seenNames = new Set();

// 1. Add marquee players (stats already set)
for (const p of marqueePlayers) {
  if (seenNames.has(p.name)) continue;
  seenNames.add(p.name);
  allPlayers.push(p);
}

// 2. Add tier A players
for (const p of tierAPlayers) {
  if (seenNames.has(p.name)) continue;
  seenNames.add(p.name);
  allPlayers.push({
    name: p.name,
    nationality: p.overseas ? (p.nat || 'Overseas') : 'India',
    isOverseas: p.overseas,
    role: p.role,
    specialization: p.spec || p.role,
    basePrice: rand(100, 200),
    tier: 'A',
    stats: generateStats(p.role, 'A')
  });
}

// 3. Add tier B players
for (const p of tierBNames) {
  if (seenNames.has(p.name)) continue;
  seenNames.add(p.name);
  allPlayers.push({
    name: p.name,
    nationality: p.overseas ? (p.nat || 'Overseas') : 'India',
    isOverseas: p.overseas,
    role: p.role,
    specialization: p.spec || p.role,
    basePrice: rand(50, 100),
    tier: 'B',
    stats: generateStats(p.role, 'B')
  });
}

// 4. Add tier C players
for (const p of tierCNames) {
  if (seenNames.has(p.name)) continue;
  seenNames.add(p.name);

  const specs = {
    Batsman: ['Right-hand Bat', 'Left-hand Bat'],
    Bowler: ['Right-arm Fast', 'Right-arm Medium', 'Right-arm Spin', 'Left-arm Fast', 'Left-arm Spin'],
    'All-Rounder': ['Right-hand Bat, Right-arm Medium', 'Left-hand Bat, Left-arm Spin', 'Right-hand Bat, Right-arm Spin'],
    Wicketkeeper: ['Right-hand Bat, WK', 'Left-hand Bat, WK']
  };

  allPlayers.push({
    name: p.name,
    nationality: p.overseas ? (p.nat || 'Overseas') : 'India',
    isOverseas: p.overseas,
    role: p.role,
    specialization: p.spec || specs[p.role]?.[rand(0, specs[p.role].length - 1)] || p.role,
    basePrice: rand(20, 50),
    tier: 'C',
    stats: generateStats(p.role, 'C')
  });
}

