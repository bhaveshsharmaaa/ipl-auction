// Stats generation helpers
// ═══════════════════════════════════════════════════════════════
function rand(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randFloat(min, max, decimals = 2) {
  return parseFloat((Math.random() * (max - min) + min).toFixed(decimals));
}

function generateStats(role, tier) {
  const matchMultiplier = tier === 'Marquee' ? 3 : tier === 'A' ? 2 : tier === 'B' ? 1.5 : 1;
  const matches = rand(Math.floor(10 * matchMultiplier), Math.floor(120 * matchMultiplier));
  
  const stats = {
    matches,
    runs: 0, battingAvg: 0, strikeRate: 0,
    wickets: 0, bowlingAvg: 0, economy: 0,
    catches: rand(2, Math.floor(matches * 0.4)),
    fifties: 0, hundreds: 0
  };

  const isBatter = ['Batsman', 'Wicketkeeper', 'All-Rounder'].includes(role);
  const isBowler = ['Bowler', 'All-Rounder'].includes(role);

  if (isBatter) {
    stats.runs = rand(Math.floor(matches * 8), Math.floor(matches * 28));
    stats.battingAvg = randFloat(18, 42);
    stats.strikeRate = randFloat(115, 160);
    stats.fifties = Math.floor(stats.runs / 280);
    stats.hundreds = Math.floor(stats.runs / 1200);
  }

  if (isBowler) {
    stats.wickets = rand(Math.floor(matches * 0.3), Math.floor(matches * 1.1));
    stats.bowlingAvg = randFloat(20, 35);
    stats.economy = randFloat(7.0, 9.5);
  }

  return stats;
}

